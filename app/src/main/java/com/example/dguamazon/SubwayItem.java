package com.example.dguamazon;

public class SubwayItem {

    private String name;
    private String subName;

    public String getName() {
        return name;
    }


    public String getSubName() {
        return subName;
    }

    public SubwayItem(String name, String subName) {
        this.name = name;
        this.subName = subName;
    }
}
